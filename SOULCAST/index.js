        // Generate movie cards
        function generateMovieCards(containerId, animeList) {
            const container = document.getElementById(containerId);
            container.innerHTML = '';
            
            animeList.forEach(anime => {
                const movieCard = document.createElement('div');
                movieCard.className = 'movie-list-item';
                movieCard.innerHTML = `
                    <img src="${anime.image}" alt="${anime.title}" class="movie-list-item-img">
                    <div class="movie-list-item-title">${anime.title}</div>
                    <div class="movie-list-item-info">
                        <p class="movie-list-item-desc">${anime.description}</p>
                        <button class="movie-list-item-button" data-title="${anime.title}">Watch Now</button>
                    </div>
                `;
                container.appendChild(movieCard);
            });
            
            // Add event listeners to watch buttons
            const watchButtons = container.querySelectorAll('.movie-list-item-button');
            watchButtons.forEach(button => {
                button.addEventListener('click', function() {
                    const movieTitle = this.getAttribute('data-title');
                    showMoviePage(movieTitle);
                });
            });
        }

